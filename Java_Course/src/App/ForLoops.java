package App;

public class ForLoops {
	public static void forLoops(String [] args) {
		
		for(int i=1; i<=10; i++) {
			System.out.println(i);
		}
		
		String [] friends = {"Bruce", "Tony", "Peter", "Thor"};
		
		for(int i=0; i<friends.length; i++) {
			System.out.println( friends[i] );
		}
		
	}
}
